#include "3-calc.h"

/**
 * op_add - adds two nember
 * @a: int
 * @b: int
 * Return: sum
*/

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - subtracts two number
 * @a: int
 * @b: int
 * Return: difference
*/

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - multiplies two int
 * @a: int
 * @b: int
 * Return: product
*/

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - divides two int
 * @a: int
 * @b: int
 * Return: quotient
*/

int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - modulo two int
 * @a: int
 * @b: int
 * Return: remainder
*/

int op_mod(int a, int b)
{
	return (a % b);
}
